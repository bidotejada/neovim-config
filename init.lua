vim.g.have_nerd_font = true

-- [[ Basic Autocommands ]]
-- Highlight when yanking (copying) text
--  Try it with `yap` in normal mode
vim.api.nvim_create_autocmd('TextYankPost', {
  desc = 'Highlight when yanking (copying) text',
  group = vim.api.nvim_create_augroup('kickstart-highlight-yank', { clear = true }),
  callback = function()
    vim.highlight.on_yank()
  end,
})
-- vertically center document when entering insert mode
vim.api.nvim_create_autocmd('InsertEnter', {
  pattern = '*', -- Applies to all files
  command = 'normal! zz', -- Executes 'zz' in normal mode
  desc = 'vertically center document when entering insert mode',
})
-- remove trailing whitespace
vim.api.nvim_create_autocmd('BufWritePre', {
  pattern = '*', -- Applies to all file types
  callback = function()
    local save_cursor = vim.api.nvim_win_get_cursor(0) -- Save cursor position
    vim.cmd [[%s/\s\+$//e]] -- Remove trailing whitespace
    vim.api.nvim_win_set_cursor(0, save_cursor) -- Restore cursor position
  end,
  desc = 'remove trailing whitespace',
})

-- Set up the Lazy plugin manager
local lazypath = vim.fn.stdpath 'data' .. '/lazy/lazy.nvim'
if not (vim.uv or vim.loop).fs_stat(lazypath) then
  local lazyrepo = 'https://github.com/folke/lazy.nvim.git'
  local out = vim.fn.system { 'git', 'clone', '--filter=blob:none', '--branch=stable', lazyrepo, lazypath }
  if vim.v.shell_error ~= 0 then
    error('Error cloning lazy.nvim:\n' .. out)
  end
end
vim.opt.rtp:prepend(lazypath)

